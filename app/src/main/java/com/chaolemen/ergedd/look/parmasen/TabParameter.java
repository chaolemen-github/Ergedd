package com.chaolemen.ergedd.look.parmasen;

public class TabParameter {

    private int offset;
    private int limit;
    private int addition_album_count;
    private String channel;

    public int getOffset() {
        return offset;
    }

    public void setOffset(int offset) {
        this.offset = offset;
    }

    public int getLimit() {
        return limit;
    }

    public void setLimit(int limit) {
        this.limit = limit;
    }

    public int getAddition_album_count() {
        return addition_album_count;
    }

    public void setAddition_album_count(int addition_album_count) {
        this.addition_album_count = addition_album_count;
    }

    public String getChannel() {
        return channel;
    }

    public void setChannel(String channel) {
        this.channel = channel;
    }
}
